// Function to get all provinces
function getProvinces() {
    global $rwanda_locations;
    return array_keys($rwanda_locations);
}

// Function to get districts for a province
function getDistricts($province) {
    global $rwanda_locations;
    if (isset($rwanda_locations[$province])) {
        return array_keys($rwanda_locations[$province]);
    }
    return [];
}

// Function to get sectors for a district
function getSectors($province, $district) {
    global $rwanda_locations;
    if (isset($rwanda_locations[$province][$district])) {
        return $rwanda_locations[$province][$district];
    }
    return [];
}

// Function to get delivery fee
function getDeliveryFee($province, $district, $sector) {
    global $rwanda_locations;
    if (isset($rwanda_locations[$province][$district][$sector])) {
        return $rwanda_locations[$province][$district][$sector];
    }
    return 5000; // Default fee
}
